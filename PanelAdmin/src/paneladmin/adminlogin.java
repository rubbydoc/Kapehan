    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        PreparedStatement st;
        ResultSet rs;
        
        String username = usera.getText();
        String password = passa.getText();
        String status = "Active";
        
        
        String query = "SELECT * FROM `employees` WHERE `email` = ?  AND `password` = ? AND `status` = ? ";
        
        try {
            st = sqlconnection.getConnection().prepareStatement(query);
            
            st.setString(1, username);
            st.setString(2, password);
            st.setString(3, status);
            
            rs = st.executeQuery();
            
            if (rs.next()) {
                
                adminpanel success = new adminpanel();
                success.setVisible(true);
                success.pack();
                success.setLocationRelativeTo(null);
                success.setDefaultCloseOperation(adminlogin.EXIT_ON_CLOSE);
                this.dispose();
                          
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Username / Password", "Login Error", 2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(adminlogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

