    public void getIncome() {

        try {

            Statement stmt = c.connect().createStatement();
            ResultSet rs = stmt.executeQuery("select Sum(total) as sumItems from transactions");

            while (rs.next()) {
                double sumItems = rs.getDouble("sumItems");

                String item = String.format("%.2f", sumItems);

//                items.setText("$" + item);
                totalincome.setText("$" + item);
//                tot = totall.getText();

            }
            c.connect().close();

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

    public void getTotalUser() {

        try {

            Statement stmt = c.connect().createStatement();
            ResultSet rs = stmt.executeQuery("select COUNT(customerID) from customers");

            while (rs.next()) {

                String count = rs.getString(1);

//                items.setText("$" + item);
                totalcust.setText(count);
//                tot = totall.getText();

            }

            c.connect().close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
