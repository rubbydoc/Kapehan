    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        String firstname = name.getText();
        String mail = email.getText();
        String password = String.valueOf(pass.getPassword());
        String confirm = String.valueOf(confirmPass.getPassword());

        if (confirm.equals(password)) {
            try {
                PreparedStatement ps = c.connect().prepareStatement("INSERT INTO employees (fullName, email,password, role, status) VALUES (?,?,?,?,?)");
                ps.setString(1, firstname);
                ps.setString(2, mail);
                ps.setString(3, password);
                ps.setString(4, "Staff");
                ps.setString(5, "to be approved");

                ps.executeUpdate();
                c.connect().close();

            } catch (SQLException e) {
            }

            JOptionPane.showMessageDialog(null, "Successfuly registered!", "",
                    JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new Login().setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Password do not match", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_signInActionPerformed

